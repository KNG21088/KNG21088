public class SumOfArrayValue {
	public static void main(String[] args) {
		int[]array =new int[]{11,12,13,14,15};
		int sum = 0;
		for (int i =0; i<array.length;i++){
			sum =sum+array[i];
		}
		System.out.println("sum of all the elements of an array:" +sum);
		                      
	}
		
		
	}





